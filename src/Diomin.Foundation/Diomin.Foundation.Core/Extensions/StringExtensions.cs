        /// </summary>
        /// <param name="connectionString">数据库连接字符串</param>
        /// <param name="toUpper">是否转大写</param>
        /// <returns></returns>
        public static string GetConnectionStringSchemaName(this string connectionString, bool toUpper = true)
        {
            if (connectionString.IsNullOrWhiteSpace() || !connectionString.Contains(";"))
                return null;
            var items = connectionString.Split(';').ToList();
            foreach (var item in items)
            {
                if (!item.IsNullOrWhiteSpace() && item.Contains("="))
                {
                    var arr = item.Trim().Split('=');
                    var name = arr[0];
                    var value = arr[1];
                    if (name.EqualIgnoreCase("user id"))
                        return toUpper ? value.ToUpper() : value;
                }
            }
            return null;
        }
