            // 若是字母则直接返回
            if (vChar >= 'a' && vChar <= 'z' || vChar >= 'A' && vChar <= 'Z')
                myStr = vChar.ToString();
            else if (vChar >= 19968 && vChar <= 40869)
                myStr = PinYin.StrChineseFirstPy[vChar - 19968].ToString();

            return myStr;
        } // 获取首字的形状拼音字母

        public static string AddFirstPinyin(this string str)
        {
            if (string.IsNullOrEmpty(str)) return string.Empty;
            var vChar = str.ToCharArray()[0];
            // 若是字母则直接返回
            if (vChar >= 'a' && vChar <= 'z' || vChar >= 'A' && vChar <= 'Z')
                return str;
            if (vChar >= 19968 && vChar <= 40869)
            {
                // 对可以查找的汉字计算它的首拼音字母的位置，然后输出
                var strNew = PinYin.StrChineseFirstPy[vChar - 19968].ToString();
                return strNew + str;
            }

            return str;
